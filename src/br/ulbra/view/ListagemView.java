    private void btnImcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImcActionPerformed
          try {
        // ALTERAÇÃO PARA VIRGULA: aceita 1,75 ou 1.75
        double peso = Double.parseDouble(txtPeso.getText().replace(',', '.'));
        double altura = Double.parseDouble(txtAltura.getText().replace(',', '.'));

            if (altura <= 0) {
                txtImc.setText("Altura inválida");
                return;
            }

            double imc = peso / (altura * altura);
            txtImc.setText(String.format("%.2f", imc));

        } catch (NumberFormatException e) {
            txtImc.setText("Preencha peso e altura!");
        }
    
    }//GEN-LAST:event_btnImcActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
      try {
        String tipoPesquisa = JOptionPane.showInputDialog(this, "Digite o tipo de treino para pesquisar:");
        if (tipoPesquisa == null || tipoPesquisa.trim().isEmpty()) {
            // Se nada for digitado, recarrega todos os treinos
            listarTreinosUsuario();
            return;
        }

        // Lista todos os treinos do usuário
        List<Treino> lista = controller.listarPorUsuario(usuarioLogado.getId());

        // Limpa a tabela
        DefaultTableModel model = (DefaultTableModel) tbTreino.getModel();
        model.setRowCount(0);

        // Adiciona apenas os treinos que contêm o texto digitado no tipo
        for (Treino t : lista) {
            if (t.getTipo().toLowerCase().contains(tipoPesquisa.toLowerCase())) {
                model.addRow(new Object[]{
                    t.getIdTreino(),
                    t.getTipo(),
                    t.getDuracao(),
                    t.getCalorias(),
                    t.getDataTreino()
                });
            }
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao pesquisar: " + e.getMessage());
    }

    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

    if (btnEditar.getText().equals("Editar")) {
            txtNome.setEditable(true);
            txtIdade.setEditable(true);
            txtPeso.setEditable(true);
            txtAltura.setEditable(true);
            btnEditar.setText("Salvar");
        } else {
        
            try {
                usuarioLogado.setNome(txtNome.getText());
                usuarioLogado.setIdade(Integer.parseInt(txtIdade.getText()));
 // ALTERAÇÃO PARA VIRGULA: aceita 1,75 ou 1.75
            usuarioLogado.setPeso(Double.parseDouble(txtPeso.getText().replace(',', '.')));       
            usuarioLogado.setAltura(Double.parseDouble(txtAltura.getText().replace(',', '.')));   
                UsuarioDAO dao = new UsuarioDAO();
                dao.atualizar(usuarioLogado); // atualiza corretamente pelo ID

                JOptionPane.showMessageDialog(this, "Dados atualizados com sucesso!");

                txtNome.setEditable(false);
                txtIdade.setEditable(false);
                txtPeso.setEditable(false);
                txtAltura.setEditable(false);

                btnEditar.setText("Editar");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro ao atualizar: " + e.getMessage());
                e.printStackTrace();
            }
        }
    


    }//GEN-LAST:event_btnEditarActionPerformed

