    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
        Usuario u = new Usuario();
        u.setNome(txtNome.getText().trim());
        u.setSenha(txtSenha.getText().trim());

        // Verifica√ß√µes
        if (txtIdade.getText().isEmpty() || txtPeso.getText().isEmpty() || txtAltura.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha idade, peso e altura com valores num√©ricos!");
            return;
        }

        u.setIdade(Integer.parseInt(txtIdade.getText()));
        u.setPeso(Double.parseDouble(txtPeso.getText()));
        u.setAltura(Double.parseDouble(txtAltura.getText()));

        UsuarioDAO dao = new UsuarioDAO();
        dao.salvar(u); // salva e j√° seta o ID no objeto 'u'

        // Abre ListagemView passando o usu√°rio completo
        new TelaInicial(u).setVisible(true);
        this.dispose(); // fecha a tela de cadastro

        JOptionPane.showMessageDialog(null, "Usu√°rio salvo com sucesso!");

        // limpa campos (opcional, j√° que fechamos a tela)
        txtNome.setText("");
        txtIdade.setText("");
        txtPeso.setText("");
        txtAltura.setText("");
        txtSenha.setText("");

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Idade, peso e altura devem ser n√∫meros v√°lidos!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao salvar usu√°rio: " + e.getMessage());
        e.printStackTrace();
    }

    }//GEN-LAST:event_btnSalvarActionPerformed

